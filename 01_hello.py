print("1. 기본형 데이터")
print(20) # 정수 데이터
print(74.4)  # 실수 데이터
print(True) # False   # 논리데이터
print(None)  #  null, 값 없음
print("2. 집합형 데이터")
print("Hello World") # 문자여 데이터, 값수정 불가(immutable)
# 리스트 데이터: 순서있고 중복허용, 값수정 가능
print(["aa@daum.net","bb@naver.com","aa@daum.net"]) 
# 셋 데이터 : 순서없고 중복불가
print({"aa@daum.net","bb@naver.com","aa@daum.net"})
# 튜플 데이터: 리스트 특징, 값수정 불가(immutable)
print(("aa@daum.net","bb@naver.com")) 
# 딕셔너리 데이터:  key:value 형태로 저장.
print({"name":"홍길동", "age":20}) 
